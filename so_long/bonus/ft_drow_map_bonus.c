#include "../so_long.h"

void ft_drow_map(t_data *data)
{
    int i, j;

    for (i = 0; i < data->x; i++)
    {
        for (j = 0; j < data->y; j++)
        {
            ft_put_image(data, i, j, data->ptr[i][j]);
        }
    }
}

